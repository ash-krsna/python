#Program for demonstrating the functionality of arithmetic operators
a = float(input("Enter Value of a:"))
b = float(input("Enter Value of b:"))
print("*"*50)
print("\tResults of Arithmetic Operators")
print("*"*50)
print("\t\tSum({},{})={}".format(a,b,a+b))
print("\t\tSum({},{})={}".format(a,b,a-b))
print("\t\tSum({},{})={}".format(a,b,a*b))
print("\t\tSum({},{})={}".format(a,b,a/b))
print("\t\tSum({},{})={}".format(a,b,a//b))
print("\t\tSum({},{})={}".format(a,b,a%b))
print("\t\tSum({},{})={}".format(a,b,a**b))
print("*"*50)



