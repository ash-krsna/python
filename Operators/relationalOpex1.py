#Program for demonstrating the Functionality of Relational Operatora
a = float(input("Enter Value of a:"))
b = float(input("Enter Value of b:"))
print('-'*50)
print("\tResults of Relational Operators")
print("-"*50)
print("\t\t{} > {} = {}".format(a,b,a>b))
print("\t\t{} < {} = {}".format(a,b,a<b))
print("\t\t{} == {} = {}".format(a,b,a==b))
print("\t\t{} != {} = {}".format(a,b,a!=b))
print("\t\t{} >= {} = {}".format(a,b,a>=b))
print("\t\t{} <= {} = {}".format(a,b,a<=b))
print('-'*50)
#Here a>b, a<b, a==b, a!=b, a>=b, a<=b, are called Relational Expressions.

